#include <stdio.h>

// 1 bit = 8 bytes


// Tipos de datos

int main() {
    // Declaración de variables
    int entero = 10;
    float flotante = 10.5;
    double doble = 10.5;
    char caracter = 'a';
    char cadena[] = "Hola, Mundo!";
    // Impresión de variables
    printf("Entero: %d\n", entero);
    printf("Flotante: %f\n", flotante);
    printf("Doble: %f\n", doble);
    print("Caracter: %c\n", caracter);
    printf("Cadena: %s\n", cadena);
    return 0;
}